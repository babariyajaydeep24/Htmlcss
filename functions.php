function create_posttype() {
register_post_type( 'booking',
// CPT Options
array(
  'labels' => array(
   'name' => __( 'Booking' ),
   'singular_name' => __( 'Booking' )
  ),
  'public' => true,
  'has_archive' => false,
  'rewrite' => array('slug' => 'booking'),
 )
);
}
// Hooking up our function to theme setup
add_action( 'init', 'create_posttype' );

/* Custom Post Type End */

/*Custom Post type start*/
function cw_post_type_movie() {
$supports = array(
'title',  // post title
'editor', // post content
'author', // post author
'thumbnail', // featured images
'excerpt', // post excerpt
'custom-fields', // custom fields
'comments', // post comments
'revisions', // post revisions
'post-formats', // post formats
);
$labels = array(
'name' => _x('Booking', 'plural'),
'singular_name' => _x('booking', 'singular'),
'menu_name' => _x('booking', 'admin menu'),
'name_admin_bar' => _x('booking', 'admin bar'),
'add_new' => _x('Add New', 'add new'),
'add_new_item' => __('Add New booking'),
'new_item' => __('New booking'),
'edit_item' => __('Edit booking'),
'view_item' => __('View booking'),
'all_items' => __('All booking'),
'search_items' => __('Search booking'),
'not_found' => __('No booking found.'),
);
$args = array(  
'supports' => $supports,
'labels' => $labels,
'public' => true,
'query_var' => true,
'rewrite' => array('slug' => 'booking'),
'has_archive' => true,
'hierarchical' => false,
);
register_post_type('booking', $args);
}
add_action('init', 'cw_post_type_booking');
/*Custom Post type end*/


